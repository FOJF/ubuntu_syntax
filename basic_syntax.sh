# 현재 위치에서 파일 및 폴더 조회
ls

# 목록 조회 자세히
ls -l

# 목록 조회 자세히, 숨김 파일까지
ls -al

# 목록 조회 자세히, 숨김 파일, 오래된 순으로 정렬 all, list, revert, time
ls -alrt

# 목록 조회 자세히, 숨김 파일, 최신 순으로 정렬
ls -alt

